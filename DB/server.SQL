DROP TABLE IF EXISTS POS_PARAMETROS
;
DROP TABLE IF EXISTS POS_SUCURSAL
;
DROP TABLE IF EXISTS POS_POS
;
DROP TABLE IF EXISTS POS_FORMAS_PAGO
;
DROP TABLE IF EXISTS POS_CONVENIOS
;
DROP TABLE IF EXISTS POS_INVENTARIO
;
DROP TABLE IF EXISTS POS_LISTA_PRECIO
;
DROP TABLE IF EXISTS POS_USUARIO
;



CREATE TABLE POS_PARAMETROS
(
	ID INTEGER NOT NULL,
	IVA INTEGER,
	DATOS_BOLETA VARCHAR(30),
	DATOS_FACTURA VARCHAR(30),
	DIAS_HISTORICO INTEGER,
	SUPERCTA VARCHAR(15),
	SERVER_BD VARCHAR(20),
	PORT_BD VARCHAR(5),
	SERVER_CENTRAL VARCHAR(20),
	PORT_CENTRAL VARCHAR(5),
	SERVER_DIOS VARCHAR(20),
	PRIMARY KEY (ID)
) 
;


CREATE TABLE POS_SUCURSAL
(
	ID INTEGER NOT NULL,
	SUCURSAL INTEGER,
	DESCRIPCION VARCHAR(30),
	DIRECCION VARCHAR(30),
	CIUDAD VARCHAR(20),
	COMUNA VARCHAR(20),
	REGION  VARCHAR(20),
	PRIMARY KEY (ID)
) 
;


CREATE TABLE POS_POS
(
	ID INTEGER NOT NULL,
	SUCURSAL VARCHAR(30),
	CODIGO INTEGER,
	PRIMARY KEY (ID)
) 
;


CREATE TABLE POS_FORMAS_PAGO
(
	ID INTEGER NOT NULL,
	CODIGO VARCHAR(20),
	DESCRIPCION VARCHAR(30),
	FRACCION INTEGER,
	PRIMARY KEY (ID)
) 
;


CREATE TABLE POS_CONVENIOS
(
	ID INTEGER NOT NULL,
	SUCURSAL INTEGER,
	RUT VARCHAR(20),
	DESCRIPCION VARCHAR(30),
	PORC_DESCUENTO INTEGER,
	FECHA_INICIO VARCHAR(8),
	FECHA_FINAL VARCHAR(8),
	PRIMARY KEY (ID)
) 
;


CREATE TABLE POS_INVENTARIO
(
	ID INTEGER NOT NULL,
	SUCURSAL INTEGER,
	CODIGO VARCHAR(20),
	STOCK INTEGER,
	STOCK_MIN INTEGER,
	PRIMARY KEY (ID)
) 
;


CREATE TABLE POS_LISTA_PRECIO
(
	ID INTEGER NOT NULL,
	CODIGO VARCHAR(20),
	SUCURSAL INTEGER,
	DESCRIPCION VARCHAR(30),
	NETO INTEGER,
	PRIMARY KEY (ID)
) 
;


CREATE TABLE POS_USUARIO
(
	ID INTEGER NOT NULL,
	USUARIO VARCHAR(30),
	SUCURSAL INTEGER,
	PASSWORD VARCHAR(20),
	TIPO_ACCESO VARCHAR(20),
	PRIMARY KEY (ID)
) 
;
