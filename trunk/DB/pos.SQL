DROP TABLE IF EXISTS POS_VENTA_DETALLE_PAGO
;
DROP TABLE IF EXISTS POS_VENTA_DETALLE
;
DROP TABLE IF EXISTS POS_VENTA
;
DROP TABLE IF EXISTS POS_FORMAS_PAGO
;
DROP TABLE IF EXISTS POS_CONVENIOS
;
DROP TABLE IF EXISTS POS_INVENTARIO
;
DROP TABLE IF EXISTS POS_LISTA_PRECIO
;
DROP TABLE IF EXISTS POS_USUARIO
;



CREATE TABLE POS_VENTA_DETALLE_PAGO
(
	ID INTEGER NOT NULL,
	NUMERO_VENTA VARCHAR(20),
	TIPO_VENTA INTEGER,
	TIPO_PAGO INTEGER,
	FECHA_VENCIMIENTO VARCHAR(8),
	BANCO VARCHAR(20),
	VALOR_PAGADO INTEGER,
	PRIMARY KEY (ID)
) 
;


CREATE TABLE POS_VENTA_DETALLE
(
	ID INTEGER NOT NULL,
	CODIGO VARCHAR(20),
	CANTIDAD INTEGER,
	PRECIO_UNITARIO INTEGER,
	NETO INTEGER,
	IVA INTEGER,
	TOTAL INTEGER,
	PRIMARY KEY (ID)
) 
;


CREATE TABLE POS_VENTA
(
	ID INTEGER NOT NULL,
	TIPO_VENTA INTEGER,
	NUMERO INTEGER,
	ID_USUARIO INTEGER,
	RUT_CLIENTE VARCHAR(20),
	PRIMARY KEY (ID)
) 
;


CREATE TABLE POS_FORMAS_PAGO
(
	ID INTEGER NOT NULL,
	CODIGO VARCHAR(20),
	DESCRIPCION VARCHAR(30),
	FRACCION INTEGER,
	PRIMARY KEY (ID)
) 
;


CREATE TABLE POS_CONVENIOS
(
	ID INTEGER NOT NULL,
	RUT VARCHAR(12),
	DESCRIPCION VARCHAR(50),
	PORC_DESCUENTO INTEGER,
	FECHA_INICIO VARCHAR(8),
	FECHA_FINAL VARCHAR(8),
	PRIMARY KEY (ID)
) 
;


CREATE TABLE POS_INVENTARIO
(
	ID INTEGER NOT NULL,
	CODIGO VARCHAR(20),
	STOCK INTEGER,
	STOCK_MIN INTEGER,
	RUT_CLIENTE VARCHAR(20),
	DESCUENTO INTEGER,
	IVA INTEGER,
	NETO INTEGER,
	TOTAL INTEGER,
	PRIMARY KEY (ID)
) 
;


CREATE TABLE POS_LISTA_PRECIO
(
	ID INTEGER NOT NULL,
	CODIGO VARCHAR(20),
	DESCRIPCION VARCHAR(50),
	NETO INTEGER,
	COSTO INTEGER,
	PRIMARY KEY (ID)
) 
;


CREATE TABLE POS_USUARIO
(
	ID INTEGER NOT NULL,
	USUARIO VARCHAR(50),
	PASSWORD VARCHAR(20),
	TIPO_ACCESO VARCHAR(20),
	PRIMARY KEY (ID)
) 
;
